<p>
C'est dans un secret le plus total que la Matawari Cie concocte depuis quelques mois leur première création. Comédie policière digne des films d'espionage des années 70', elle est le fruit de la rencontre de Barnaby Gibbons (Mr Green & Mr Orange), Olivier Miraglia (Les Figues de Barbaries) et Stéphane Marin alias "Cynematik" (Espaces Sonores). Ce trio a émergé au sein de la compagnie Le Phun, de ses projets et créations, avec l'envie de radicaliser un choix scénographique: l'absence de décor.
</p>
<br/>
<br/>
<h2 style="text-align: center">
Entretien:
</h2>
<p>
<img class="left" width="200" src="../../img/residences/manchot_son.jpg" alt="Photo spectacle"/>
"Dans le film « Arizona Dream » d'Emir Kusturica, une scène a particulièrement retenu notre attention : elle se déroule dans un cabaret. On y découvre, un comédien qui reprend la scène mythique de « Mort aux trousses » d'A. Hitchcock où un avion harcèle Cary Grant. Dans son numéro, seul le son de cet avion et le jeu de comédien suffisent à planter le décor et à faire revivre la scène."</p>
<p>

 "C'est, en résumé, l'alliance de ces deux techniques (dramatique et sonore) qui suscite notre intérêt. En effet, concevoir un projet où la dynamique scénique n'est animée que par le jeu d'acteur et l'univers sonore constitue une forme artistique minimale qui nous semble pertinente. C'est cette forme légère et épurée que nous voulons travailler, explorer, et surtout, partager."</p><p style="text-align:right"><em>Matawari Cie</em></p>
</p>

<br/>
<br/>
<br/>
<p>
<img class="right" width="200" src="../../img/residences/manchot_machines.jpg" alt="Photo spectacle"/>

Au milieu de ce climat de tension et de rejet qu'alimente la Guerre Froide, nos deux héros,  J.U.L.E.S., agent n°1 des Services de Documentation Extérieure et de Contre-espionnage de l'Etat français et Captain Hastings : Secret  Agent au service de la Couronne britannique, ancien de la RAF, se font les porte-drapeaux de la tolérance et de la cohésion. Leurs aventures invraisemblables, leurs rencontres plus farfelues les unes que les autres ne sont que le prétexte de la  rencontre de l'Autre, l'accueil de sa différence radicale, l'envie de son altérité inaliénable. Les dangers qu'ils rencontrent sont toujours évités en douceur, avec diplomatie, et beaucoup d'humour, leur seule arme, leur unique protection. 
</p>
<br/>
<p>
En dehors du baroque et de la folie de cette époque délurée..., c'est davantage le sentiment d'ouverture d'esprit, de recherche de nouveauté, de libération des moeurs et des esprits animant la période qui nourrit  le propos du Vol du Manchot.
</p>
