<h2>A l'origine du concert ...</h2>
<p>
...il y a l'envie d'interroger cette forme de « spectacle vivant », qui nous apparaît comme un
véritable théâtre du son. <br/><br/>
Si l'émotion musicale y joue le rôle principal, elle est intimement liée à ce que nos sens perçoivent. La présence des musiciens, comme l' attention de l'auditoire, contribuent à l'intensité et à la fragilité de ce moment éphémère, au même titre que la Musique elle-même.
</p>
<img class="left" width="200" src="../../img/residences/Concert-4WEB.gif" alt="Photo concert"/>
<p>
Nous avons voulu glisser dans cet équilibre instable les grains de sable de notre imagination, notre exigence de spectateurs, nos doutes de musiciens et une forte tendance à rire quand l'art sérieux oublie d être généreux.<br/>
Nous envisageons donc le concert comme une situation théâtrale, au sens le plus large.<br/><br/>
Ce qui le constitue ?<br/> Des visages, justement, qui ne se cachent pas derrière la musique, mais jouent avec comme des acteurs avec leur texte. Il ne s agit plus de tendre à la perfection, cette chose technique qui place parfois l interprète dans une terrible solitude, et laisse le spectateur frustré de toute émotion. Il s agit d accorder plus d attention à ...l intention.
</p>


<p>
Chaque « épisode » de ce spectacle est né de la rencontre entre un désir musical et une idée de
mise en scène, deux matières qui s'entrechoquent. Les musiciens, pris dans leurs propres « variations sur un thème ... » y passent du rôle d'interprète réaliste à celui de créature imaginaire.<br/>
L'univers sonore évolue de même: les instruments se mêlent à la bande électroacoustique, et le son bondit d un plan à l'autre, tantôt toile de fond, décor bruitiste, tantôt acteur à part entière, au centre de l'attention.
</p>
<br/>

<p>
Parmi nos sources d'inspiration : le quotidien du musicien, les conventions du spectacle, les
personnages « inadaptés » du cinéma burlesque, le rituel débordé par l'imprévu. En parallèle, l'écriture musicale s'est inspirée d'une approche chorégraphique très rythmique. Le choix d'être à la fois compositeurs et interprètes nous permet d'habiter complètement la partition, de la remodeler à l'envi.
</p>
<br/>
<p>
À l'origine du Concert... il y a un plaisir à écrire, à jouer, et des questions qui se posent quant à la création et la transmission aujourd'hui. Nous proposons ici quelques tentatives de réponse.
</p>
