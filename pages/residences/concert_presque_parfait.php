<h2>Sept tableaux sonores...</h2>


<p>
...comme les portraits vivants de musiciens imaginaires,<br/>
croqués à leur insu, ou prenant la pose, ils apparaissent :<br/>
Virtuoses, maladroits, angoissés ou détendus,<br/>
mais tous pris sur le vif, quelque part,<br/>
en scène, en songe, en répétition<br/>
voire en pleine mutation !
</p>

<br/>
<p>
Ils ont en commun de participer, sans le savoir peut-être,<br/>
au concert de ce soir.<br/>
Conviés sur scène avec tout leur environnement sonore,<br/>
leurs trucs, tracs et tracas, bref, leur totale perfection,<br/>
<img class="right" width="200" src="../../img/residences/Concert-3WEB.gif" alt="Photo concert"/>

ils se retrouvent pris entre désir musical<br/>
et désordre trop humain.


</p>
<br/>
<p>

Nous sommes donc bien au concert, où chaque pièce cherche son équilibre entre partie instrumentale, électroacoustique et vocale. Mais nous sommes aussi au théâtre, là où nos sens embrassent d'un coup le geste, le rythme, l'espace, l'intention et le mouvement chorégraphique.
<br/>

<img class="left" width="200" src="../../img/residences/Concert-1WEB.gif" alt="Photo concert"/>
<br/>

Un théâtre musical, donc.<br/>
Où Tati pourrait croiser Aperghis,<br/>
où l'impro ivre côtoie la sobre partition,<br/>
où le jeu de l ouïe est aussi celui du regard<br/>
</p>
<br/>
<br/>
<p>
Ce concert est avant tout une expérience qui donne une grande place à l'humour, à l'illusion et au mystère.<br/>
Le fil du spectacle ne suit pas une narration continue, mais relie des situations simples, riches en rebondissements, comme des courts-métrages traversés par deux créatures sonores qui se cherchent et s'accrochent.<br/>
Pour incarner ces « percussionnages » à multiples casquettes, Guillaume Hermen et Loïc Bescond revisitent leur expérience de musiciens et de spectateurs, pour en extraire des situations drôles, étranges et si possible poétiques.
</p>
<br/>
<br/>
<p>
Entre les parenthèses de ce Concert (presque) Parfait, il y a un espace suspendu où l'interprète jouant sa musique s'aperçoit soudain qu il est aussi joué par elle.<br/>
<br/>
Écoutez voir...
</p>
