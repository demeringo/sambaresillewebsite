<h1>Création 2009 ? La Pieuvre !!!</h1>
<br/>
<p>
En 15 ans d'existence Samba Résille a arpenté les villes et villages de France, d'Europe et d'Afrique du Nord. Donnant la voix (percussive !) à des projets artistiques tels que <a href="http://sambistas.online.fr/encontro99/index.html">l'Encontro</a> (rencontre Européennes de Samba (une des 1ères créations « made in Samba Résille »), les 2000 coups de minuit (<a href="http://www.culture.gouv.fr/culture/actualites/2000cdminuit/presentation.htm">Cie Transe Express</a>), Batucada 2000 (création musicale collective avec la <a href="http://www.mugar.info/Index3.htm">Cie Mugar</a> et la <a href="http://www.vieussens.com/">Cie Vieussens</a>), les couleurs Sonores (création musicale et graphique), puis à l'occasion de production artistique diverses (<a href="http://www.lesjuliettes.com/historique.htm">samba Parade de Pennautier</a> 2003 et 2004, samba Parade de Marseille en 2005), ou encore de rencontres atypiques (<a href="http://www.dailymotion.com/jlbubb/video/x3gywt_la-mouche-avec-sambaresille_music">les Bubblies</a>) ... la Troupe Samba Résille se lance désormais dans de la création originale !!
</p>
<br/>

<br/><p>En restant à cheval entre le rêve et la réalité, cette dernière création mélange spectacle de rue, spectacle musical, mime, sketches et calembour acoustiques, au service d'une bête échouée, qui déambule dans un monde nouveau pour elle, à la recherche de rencontre, de protection, et qui, on ne sait jamais vraiment quand, réserve bien des surprises à ceux qui se risquent à la suivre...
</p>
