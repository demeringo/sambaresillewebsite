<h1 style="text-align:left">
Bio
</h1>
<p>Grégory Elbaz est avec Guillaume Trouillard l'un des membres fondateurs des éditions la Cerise et de la revue Clafoutis, basée à Bordeaux.
</p>
<br/>
<p>
<img class="left" width="300" src="../../img/expo/Expo-Bix1.jpg" alt="Bix expo"/>
Après avoir publié un premier album/CD sur Oscar Peterson aux éditions Nocturne, il s'attaque ici à la vie mouvementée de Bix Beiderbecke, légende de la musique, et premier jazzman blanc original.
</p>

<p>
Cette exposition nous convie donc à découvrir l'extraordinaire travail au pastel de Grégory Elbaz, à travers ses dessins originaux en grand format. Car ce jeune auteur cultive avec ses collègues de clafoutis, un indéniable attrait pour l'art de la composition et de la narration, un talent certain dans le dessin pur.</p>
