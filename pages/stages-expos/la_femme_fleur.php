<p>
Exposition individuelle et collective, dans le cadre de TRAVERSEES AFRICAINES.<br/>
 « La Femme fleur s'invite » est un projet porté en décembre 2009 par Nadi.H.cas, auprès des artistes du Village des Arts de Dakar-Yoff.
</p>
<p>
En proposant aux artistes du village un même cadre dexpression, figuré par un format 30x30cm, sans imposer un thème, elle a recueilli 28 oeuvres originales, sur lesquelles, en présence des créateurs et avec leur accord, La Femme Fleur s'est invitée.
</p>
<p>
Au nom de ces artistes, Nadi.h.cas  se retrouve maintenant dépositaire de ces oeuvres « partagées » quelle va exposer dans divers lieux, avant de les retourner avec un livre reprenant lensemble du projet, au Village des Arts de Dakar-Yoff.
</p>
<p>
<strong>Vernissage le 4 novembre à 18h30.</strong>
</p>
