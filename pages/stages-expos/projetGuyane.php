<h1 style="text-align:center">Projet GUYANE</h1>
<h2 style="text-align:center">Exposition photosonore sur la culture guyanaise par Chercheurs d'autres</h2>
<h2 style="text-align:center">du 30/04/09 au 30/05/09</h2>

<p>
L'association <a href="http://www.chercheursdautres.com/">Chercheurs d'Autres</a> a effectué un voyage de deux mois au coeur du territoire guyanais, à la recherche de ses différents visages et de ses multiples cultures. Nous avons ainsi pu rencontrer les peuples qui composent la mosaïque guyanaise, au travers du prisme de leurs musiques traditionelles.
</p>
<p>
L'exposition présente un <strong>autre regard</strong> de la société guyanaise, loin des clichés proposés régulièrement dans les médias, montrant la richesse peu commune d'un territoire méconnu.</p>
<p>
L'exposition sera <strong>photosonore</strong>, mettant en résonnance images et sons, dans un dispositif d'immersion invitant le visiteur a devenir acteur de sa visite et partie prenante du voyage.
</p>
<br/>
<br/>
