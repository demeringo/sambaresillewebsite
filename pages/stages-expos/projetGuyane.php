Une soirée débat sera également proposée le <em>jeudi 14 Mai, à 19h00</em>, au CICC. Ce sera l'occasion de mettre en perspective la situation actuelle de la Guyane, ainsi que des autres territoires d'outre-mer, au travers des thèmes de l'identité et des représentations mutuelles.
</li>

<li>
Enfin une <em>assemblée générale de l'association aura lieu le Samedi 2 Mai à 14h30</em>, ce sera l'occasion de discuter du projet associatif et d'élir le CA afin de développer notre association. Tout le monde est bienvenu, nous avons besoin d'idées fraîches et de nouvelles forces vives . Pour adhérer c'est simple contactez nous à chercheursd'autres@gmail.com (5 euros l'adhésion)
</li>
