<h1 style="text-align:center">Lulie l'exploratrice</h1>
<h2 style="text-align:center">La ville Botanique</h2>
<h2 style="text-align:center">Du 29 novembre 2007 au 11 janvier 2008</h2>

<p>
Vous connaissez certainement les jardins botaniques, où sont préservées des centaines d'espèces de plantes, locales ou exotiques, et que l'on voit exposées dans leur environnement naturel ou reconstitué...</p>
<img class="center" width="400px" src="../../img/expo/caninus.jpg" alt="Photo expo"/>
<p>
La Ville Botanique propose une étude botanique et zoologique du milieu urbain, c'est à dire de la ville. Celle-ci révèle au jardinier amateur un peu sagace les dizaines d'espèces d'abribus, fleurs d'asphalte et autres végétaux inattendus qui fleurissent l'espace urbain.</p>
<p>On y découvre également la faune (oiseaux mécaniques, grues à l'ouette, caddies...) et les insectes (gendarmes, fourmiliers...) qui se déploient dans ce biotope inventé par l'Homme. Ce musée naturaliste se veut exhaustif et présente l'ensemble des éléments constituant cet univers qui nous est si familier : de la météo des villes à un mobilier urbain adapté à la contemplation de cette nature citadine, domestiquée et hygiénique...</p>

<br/>
<p>
La Ville Botanique pose un autre regard sur la cité, celui d'une jardinière des pavés, dans une poésie urbaine à l'esprit humoristique et décalé.</p>
<p style="text-align:center">
Tout public à partir de 10 ans, entrée libre
</p>
