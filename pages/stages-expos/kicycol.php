<h1 style="text-align:center">Kicycol</h1>
<h2 style="text-align:center">Du 17 septembre au 24 octobre 2009</h2>

<img class="center" src="../../img/expo/kicycol_flyer.gif" alt="Flyer expo Kicycol"/>

<p>
<strong>Kicycol</strong> aurait pu être un simple nom emprunté par un particulier pour se donner un style, mais en fait non ... Il se veut que kicycol a vu le jour en tant qu'association en 2002, en région PACA autour de 5 personnes passionnées par les Arts Graphiques. <a href="http://www.collectif-kicycol.org/">http://www.collectif-kicycol.org</a>
</p>

<p>
Basée à <strong>Toulouse</strong> depuis 2006 Kicycol regroupe à ce jour <strong>4 personnes</strong>:<br/>
<img class="right" width="150" src="../../img/expo/kicycol_logo.gif" alt="logo expo Kicycol"/>
<strong>
- 6ril // Collage et Graphisme <br/>
- Smikart // Brikograff <br/>
- Bood'mit // Illustration <br/>
- Flavio // Pochoir <br/>
+ guest pour l'occasion<br/>
Molomolo / illustration<br/>
</strong>
</p>


<p>
<strong>L'association</strong> ou <strong>Collectif</strong> est à ce jour plus concentré sur <strong>les expositions dans différents lieux</strong> (Bar, salle, galerie, médiathèque...), les stands dans des <strong>manifestations culturelles</strong>, (Festival, concert, Mjc, Exposition collective...) les <strong>rencontres</strong> et <strong>les échanges</strong> de techniques gravitant autour des <strong>Arts graphiques</strong>.
</p>
<p>
Voilà donc, pour vous informer, quelques lignes sur le passé de kicycol, le mieux étant de venir voir l'exposition et de découvrir ainsi ce qu'on peut appeler notre (ré)création graphik ! </p>
<p>au plaisir !</p>

