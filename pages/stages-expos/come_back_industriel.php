<h1 style="text-align:left">Bio</h1>
<p>Aurélien Escuriol, 29 ans, se consacre à la photographie depuis une dizaine d'années, il travaille actuellement comme technicien lumière et régisseur sur des créations théâtrales et musicales (la troupe du "Phenix", "Sea Girls", "Théâtre de l'Atelier").</p>
<br/>
<p>Après l'exploration du noir et blanc, de la prise de vue au développement, il entame une année d'étude qui lui font découvrir la photographie couleur. Une de ses techniques de prédilection est le traitement croisé, ce procédé est  une inversion au développement du film qui donne un effet saturé aux couleurs et un contraste très élevé.
</p>
<p>
Les décors qu'il utilise pour la mise en scène de ses prises de vues sont des lieux abandonnés : usines, écoles, chantiers, maisons...
<p>
L'esprit fantomatique qui habite ces lieux sont sa principale source d'inspiration, cette exploration temporelle, ces "flash back" dans des lieux oubliés, sont les chemins qui mènent à la construction de ses photos. Dans son travail, c'est toute un imaginaire qui se construit autour de la vie des lieux tels qu'ils existaient. Bien souvent de nombreux objets, des machines de production ont été laissées ça et là par les anciens occupants. Parfois, les lieux tels qu'il les découvre donnent l'impression d'avoir été abandonnés du jour au lendemain. Il retrouve sur place des outils, des correspondances, des produits divers et variés  que les personnes utilisaient dans leur travail au quotidien (des colorants, du papier comme à l'usine Job, des habits des peaux de travail dans une tannerie à Mazamet, un rang de néon pour le séchage des peintures dans l'usine Renault de Boulogne Billancourt...). L'utilisation de ces bribes de quotidiens fait partie intégrante du processus de création artistique de son oeuvre photographique. Les objets et lieux sont réutilisés avec un modèle (un corps) qui leur redonne vie. Le travail avec des modèles non professionnel est une des caractéristiques de l'artiste. Les séances de travail se déroule sur une journée voir sur plusieurs et demande opiniâtreté pour l'artiste et son modèle.
</p>
<p>
Au travers de son oeuvre Aurélien Escuriol met ainsi en scène des fragments de passé oublié et donne une seconde vie à des lieux qui ont eu un passé florissant.
</p>

<br/>
