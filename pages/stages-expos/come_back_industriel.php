<h1 style="text-align:left">
L'exposition</h1>
<p>Ce travail de « portraits désaffectés » a commencé en 1998 et se poursuit aujourd'hui avec toujours encore plus de passion pour la mise en scène, la direction des modèles, et la recherche de nouveaux lieux de moins en moins accessibles. 
</p>
<br/>
<p>     Toutes les photographies présentées ici ont été prises avec un appareil argentique format 135mm en utilisant la lumière naturelle des lieux. La technique utilisée est celle du « traitement croisé » qui consiste à effectuer les photographies avec un film positif (E6) et de le développer dans la chimie du film négatif (C41). Le résultat donne une image contrastée avec un grain proche de la peinture picturale et aux couleurs saturées.
</p>
<p>        Tous les portraits présentés ici sont des prises de vue directes, aucun traitement numérique et aucun recadrage n'a été ajouté après la prise de vue et le développement chimique (excepté deux photographies « Les Siamoises » et « Les Tubes » qui utilisent un effet « miroir » numérique).
</p>
<p>
    Il a eu une période de prise de vue sur Toulouse et en Midi-Pyrénées, notamment dans des entreprises de Castres et Mazamet, dans l?usine Job et dans des infrastructures touchées par la catastrophe d?AZF.  
    </p>
