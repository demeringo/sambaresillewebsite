<br/>
<p>
Parmi ses derniers photo-reportages,un travail remarquable sur le Rwanda, 15 ans après le génocide.<br/>
<br/>
A des portraits de survivants et des instantanés de la vie quotidienne aujourd'hui Rwanda, l'exposition mèle des photos qui donnent la mesure de la dure réalité du génocide (lieux de mémoires, cimetières), et du travail de mémoire, de justice et de réparation qu'elle nécessite (prisons, tribunaux « Gacaca », etc).
</p>
