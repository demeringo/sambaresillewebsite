<h1 style="text-align:center">Indiens d'amazonie</h1>
<h2 style="text-align:center">à partir du 3 juin</h2>
<h2>Expo photographique  par Zia Zeff</h2>
<p>

<img class="left" width="250px" src="../../img/expo/Chaman.gif" alt="Chaman"/>
<strong>Immersion chez les détenteurs d'un savoir médicinal ancestral. Les indiens d'Amazonie luttent pour sauver leur culture unique et préserver leur forêt, poumon et écosystème le plus riche de notre planète.</strong> Cette jungle tropicale, dont 15% ont déjà disparu, joue un rôle essentiel dans la stabilisation du climat mondial. Les indiens Shipibos, vénèrent les esprits de la Nature lors de cérémonies (d'Ayahuasca) dédiées à la Terre Mère, la Pachamama.
</p>
<br/>
<br/>
<p>
<strong> 
Les photographies de Zia Zeff sont des invitations au voyage, des témoignages de rencontres.</strong> Passionnée et curieuse du monde qu'elle parcourt sans relâche à la découverte des autres et de nouvelles expériences. Après des études d'Art, de Biologie, d'Anthropologie et de Cinématographie, elle revient à son premier amour : la Photographie.
</p>
<p>
D'origine Argentine, <strong>elle possède une relation privilégiée avec l'Amérique Latine et tout particulièrement avec les tribus d'Amazonie où elle passera de nombreux mois immergée dans la jungle.</strong> 
</p>
<p>
Déterminée à construire un corpus documentaire, elle se laisse porter par les rencontres pour les mettre en forme au gré de ses découvertes de personnages qui dessinent des possibilités...
</P>
<p> 
D'ailleurs elle ne peut envisager un reportage sans portrait, « ceux sont les regards qui m'intéressent, les gens qui vivent et portent leur vie devant mon objectif aux yeux de tous».
</p>
<br/>
<br/>
<p> 
<strong>Les reportages de Zia sont toujours de longues investigations;</strong> elle affirme d'ailleurs « ne pas travailler 'sur' 'les gens mais 'avec' eux ». En effet, chaque fois qu'un sujet l'interpelle, elle prend contact avec des associations locales et travaille en bénévolat avec elles, comme en Amazonie avec l'association Depaso-Amasoa qui aide à la 'reforestation' de la forêt amazonienne au Pérou ou en Inde comme infirmière des rues pour l'association Agir pour Bénarès. Bien qu’humaniste et sociale, la photographie de cette artiste est avant tout un appel aux émotions. Elle aime la frontalité, le jeu face à l’objectif, une forme de rigueur dans la prise de vue qui lui ouvre d’autres libertés.<strong> Par l'"acte photographique", elle cherche à comprendre et à témoigner d'une réalité complexe, à se rapprocher de l'autre tout en informant, avec une subjectivité assumée.</strong>
</p>



