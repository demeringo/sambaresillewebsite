<h1 style="text-align:center">Que contas ?</h1>
<h2 style="text-align:center">13, 20, 27 et 31 janvier 2009</h2>
<h2>Des enfants créateurs présentent un regard sur leur monde.</h2>
<p>Ces créations sont le fruit d'ateliers proposés à Medellin  en Colombie par le « <strong>Collectif d'Actions Solidaires</strong> » en collaboration avec la corporation éducative « <strong>Combos</strong> » et le groupe « <strong>Jovenes Construyendo Futuro</strong> ».</p>
<br/>
<img class="center" width="400px" src="../../img/expo/que_contas.jpg" alt="Flyer que contas"/>
<br/>
<p>Un groupe d'enfants et adolescents vous présentent leurs créations réalisées lors d'ateliers proposés pas le Collectif d'Actions Solidaires (C.A.S.) au sein de la Corporation Educative Combos et du groupe Jovenes Construyendo Futuro.</p>
<br/>
<p>Ces ateliers de <strong>cinéma d'animation, photographie et vidéo</strong> ont pour objectif, par la sensibilisation/initiation à ces techniques, de proposer des <strong>moyens d'expressions libres à un public y ayant difficilement accès</strong>, Ces techniques couramment utilisées par les mass-médias dans des objectifs communicationnels sont ici utilisées pour permettre à des groupes d'enfants et de jeunes, vivant dessituations délicates, de <strong>s'exprimer sur les sujets de leurs choix, de transmettre leurs visions sur leurs mondes</strong>.</p>
<br/>
<p>Ce projet d'échange socioculturel à l'initiative du C.A.S. a été réalisé par 3 intervenants sous la forme d'un <strong>bénévolat inter-associatif à Medellin, en Colombie, de Juin à Novembre 2009.</strong></p>
<br/>
<p>Des <strong>ateliers tout public de cinéma d'animation et de photographie sténopé</strong> sont proposés tous <strong>les mercredis après-midi de janvier (les 13, 20, 27, de 14h à 19h</strong>, avec <strong>présentation des créations le dimanche 31</strong>), au sein de l'espace d'exposition (de Samba Résille) afin de permettre à qui le souhaite de s'exprimer par ces médias, et si ils le souhaitent de transmettre un retour aux créateurs des réalisations exposés.</p>

<h2>Vernissage vendredi 8 janvier à 19h</h2>
<p>
<ul>
<li>Présentation de l'<strong>action</strong> et du <stong>projet du Collectif d'Actions Solidaires</strong> par les intervenants.</li>
<li><strong>Exposition de photos</strong> réalisées par <strong>les enfants et jeunes</strong> lors des ateliers.</li>
<li><strong>Projections des films</strong> réalisés par la <strong>corporation éducative Combos</strong>, des <strong>courts-métrages</strong> réalisés par <strong>les enfants et éducateurs de Combos</strong> lors des ateliers.</li>
<li>Exposition du <strong>reportage photo</strong> réalisé durant le projet.</li>
<li>Dans une volonté <strong>d'échanges et de discussions</strong>.<br/></li>
</ul>
</p>

