		<h1>Les cours et ateliers enfant - Saison 2007/2008</h1>
<p><strong>Nos ateliers notre pédagogie</strong></p>
<br>
<p>Depuis 1998, nous proposons des ateliers autour de la pratique de la batucada.</p>
<p>Ces ateliers ont, pour l'enfant, outre la pratique musicale, plusieurs objectifs majeurs :</p>
<ul>
    <li>Enrichir son patrimoine culturel en découvrant la musique brésilienne,</li>
	<li>Développer sa capacité d'écoute et de respect de l'autre,</li>
	<li>S'approprier son temps et son espace de vie en devenant acteur dans les animations de quartier ou de village,</li>
	<li>Être valorisé au travers d'un spectacle élaboré en commun.</li>
</ul>
<p>La programmation des ateliers de la saison 2007/2008 est établie. Les ateliers commençeront la première semaine d'octobre.</p>
<p>Contactez-nous au 05 34 416 216 ou par mail à <a href="mailto:CICC@samba-resille.org">cicc@samba-resille.org</a> pour toute info et tarifs.</p>
	
	<h2>Éveil musical</h2>
    <img class="right" src="../img/cours/eveilmus.jpg" alt="groupe eveil musical"/>
   	L'art de jouer de ses sensations spontanées.
    <p>Éveil musical aux maternelles : 
l'art de jouer de ses sensations spontanées</p>
<p>Le goût qu'un enfant a de la musique est naturel, sans doute parce que, des cinq sens, c'est l'ouie 
qu'il a développé en priorité.</p>
<p>Notre rôle d'initiateurs à la musique est d'aider l'enfant à organiser et à maîtriser des sensations spontanées.</p>
<br>
<p>Pour cela, nous nous sommes fixés les objectifs suivants :</p>
<ul>
<li>L'invention et l'imagination,  </li>
<li>La notion d'esthétique,</li>
<li>Le développement de l'attention et de la mémoire,</li>
<li>Le développement de la socialisation (soi parmi les autres, avec les autres, devant les autres),</li>
<li>La mise sur un même plan des différentes formes de langage.</li>
<p>L'apprentissage de chansons, l'écoute, la compréhension et le codage d'oeuvres musicales variées, l'expression corporelle, la manipulation d'instruments, la création de paysages sonores seront les moyens essentiels que nous utiliserons pour initier l'enfant à la musique.</p>
