<h1 style="text-align:center;color:#1200A5"><i>-- Les inscriptions sont closes --</i></h1>
<p>
Samba Résille a développé son <strong>école associative</strong> toujours dans le même but solidaire et citoyen de donner accès à l'art. Que l'on soit virtuose, ou plutôt "gauche" de la musique, <strong>chacun trouve sa place</strong>, parce qu'on ne vient pas à Samba Résille pour ressortir diplomé de Samba, on vient y découvrir un patrimoine culturel riche et varié, d'influences multiethnique tout comme l'est le pays qui l'abrite.<br/>
Les cours débutants traversent en musique les principales influences du Brésil, les cours confirmés quand à eux, font escale à Bahia au son de ses rythmiques afros!
</p>
<p>
<strong>La pédagogie</strong> a été inventée pour respecter l'accessibilité à tous et toutes, elle est <strong>orale et corporelle</strong>. Explorer le rythme, travailler l'indépendance gauche droite, pied main, enrichir son patrimoine culturel, travailler la solidarité et la complémentarité des individus et du groupe, sont les objectifs principaux du processus d'apprentissage. Ainsi, <strong>aucun pré-requis musical n'est demandé</strong> à votre arrivée.
</p>
<p>
